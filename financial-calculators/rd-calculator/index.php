<h3 dir="ltr">Formula to Calculate the RD Maturity Amount</h3>
<p dir="ltr">To calculate the maturity amount of RD, below is the simple formula of this calculator for your ease. You can manually calculate the interest by putting the values in, or use our dedicated calculator that will give you the accurate results in the blink of an eye.</p>
<p><strong>&nbsp;</strong></p>
<h3 dir="ltr">Formula:</h3>
<p dir="ltr">A = P*(1+R/N)^(Nt)<br><br></p>
<p dir="ltr">where;<br><br></p>
<p dir="ltr">A = Maturity Amount</p>
<p dir="ltr">P = RD Instalment each month</p>
<p dir="ltr">N = Compounding Frequency (no. of quarters)</p>
<p dir="ltr">R = RD interest rate (%)</p>
<p dir="ltr">t = Tenure</p>
